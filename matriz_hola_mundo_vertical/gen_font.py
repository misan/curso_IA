import json
chars = {
    'H': [0x41, 0x41, 0x7F, 0x41, 0x41],
    'o': [0x38, 0x44, 0x44, 0x44, 0x38],
    'l': [0x00, 0x41, 0x7F, 0x40, 0x00],
    'a': [0x20, 0x54, 0x54, 0x54, 0x78],
    ' ': [0x00, 0x00, 0x00, 0x00, 0x00],
    'M': [0x7F, 0x02, 0x0C, 0x02, 0x7F],
    'u': [0x3C, 0x40, 0x40, 0x20, 0x7F],
    'n': [0x7C, 0x04, 0x04, 0x04, 0x78],
    'd': [0x38, 0x44, 0x44, 0x48, 0x7F],
    '.': [0x00, 0x60, 0x60, 0x00, 0x00]
}

# The dictionary above is vertical columns!
# We want horizontal rows (1 byte per row, 7 rows).
rows = {}
for c, cols in chars.items():
    c_rows = []
    for row in range(7):
        b = 0
        for col in range(5):
            if (cols[col] & (1 << row)) != 0:
                b |= (1 << col)
        c_rows.append(b)
    rows[c] = c_rows

print("const uint8_t font[128][8] = {")
for i in range(128):
    ch = chr(i)
    if ch in rows:
        print(f"  /* {ch} */ {{ " + ", ".join(f"0x{b:02x}" for b in rows[ch]) + ", 0x00 },")
    else:
        print(f"  /* {ch} */ {{ 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00 }},")
print("};")
